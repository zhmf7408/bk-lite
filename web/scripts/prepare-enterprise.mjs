#!/usr/bin/env node
import path from 'path';
import { fileURLToPath } from 'url';
import fs from 'fs-extra';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const webRoot = path.resolve(__dirname, '..');
const enterpriseWebLink = path.resolve(webRoot, 'enterprise');
const enterpriseWebRoot = fs.existsSync(enterpriseWebLink) ? fs.realpathSync(enterpriseWebLink) : enterpriseWebLink;
const routesManifestPath = path.join(enterpriseWebRoot, 'manifests', 'routes.json');
const appRoot = path.join(webRoot, 'src', 'app');
const generatedRouteRoot = path.join(appRoot, '(enterprise)');

const routePathToShimFile = (routePath) => {
  const normalized = routePath.replace(/^\/+|\/+$/g, '');
  const segments = normalized ? normalized.split('/') : [];
  return path.join(generatedRouteRoot, ...segments, 'page.tsx');
};

const renderAssembledPage = (sourceContent) => `/* This file is generated by scripts/prepare-enterprise.mjs. */

${sourceContent}`;

const validateRouteManifest = (routes) => {
  if (!routes || typeof routes !== 'object' || Array.isArray(routes)) {
    throw new Error('enterprise routes manifest must be an object keyed by route path');
  }
};

const cleanupGeneratedRoutes = async () => {
  await fs.remove(generatedRouteRoot);

  if (!(await fs.pathExists(appRoot))) {
    return;
  }

  const appEntries = await fs.readdir(appRoot, { withFileTypes: true });
  for (const appEntry of appEntries) {
    if (!appEntry.isDirectory() || appEntry.name.startsWith('(')) {
      continue;
    }

    await fs.remove(path.join(appRoot, appEntry.name, '(pages)', '(enterprise)'));
  }
};

const generateRouteShims = async (routes) => {
  await cleanupGeneratedRoutes();

  for (const [routePath, config] of Object.entries(routes)) {
    if (!routePath.startsWith('/')) {
      throw new Error(`enterprise route must start with "/": ${routePath}`);
    }

    const source = typeof config === 'string' ? config : config?.source;
    if (!source) {
      throw new Error(`enterprise route "${routePath}" is missing a source`);
    }

    const sourcePath = path.resolve(enterpriseWebRoot, source);
    if (!sourcePath.startsWith(enterpriseWebRoot)) {
      throw new Error(`enterprise route source must stay within web/enterprise: ${source}`);
    }

    if (!(await fs.pathExists(sourcePath))) {
      throw new Error(`enterprise route source not found for "${routePath}": ${sourcePath}`);
    }

    const shimFilePath = routePathToShimFile(routePath);
    await fs.ensureDir(path.dirname(shimFilePath));
    const sourceContent = await fs.readFile(sourcePath, 'utf8');
    await fs.writeFile(shimFilePath, renderAssembledPage(sourceContent), 'utf8');
  }
};

export const prepareEnterpriseRoutes = async () => {
  if (!(await fs.pathExists(enterpriseWebRoot))) {
    await cleanupGeneratedRoutes();
    console.log('ℹ️ No web/enterprise link found, skipping enterprise route assembly.');
    return;
  }

  if (!(await fs.pathExists(routesManifestPath))) {
    await cleanupGeneratedRoutes();
    console.log('ℹ️ No enterprise route manifest found, skipping enterprise route assembly.');
    return;
  }

  const routes = await fs.readJSON(routesManifestPath);
  validateRouteManifest(routes);
  await generateRouteShims(routes);
  console.log('✅ Enterprise route shims prepared successfully.');
};

if (process.argv[1] === __filename) {
  prepareEnterpriseRoutes().catch((error) => {
    console.error('Failed to prepare enterprise routes:', error);
    process.exitCode = 1;
  });
}
